void main(List<String> args) {
  // perbandingan string
  String text1 = "Cuma tulisan";
  String text2 = "Tulisan cuma";

  print("Text1 == Text2 ${text1 == text2}");
  print("Text1 != Text2 ${text1 != text2}");

  // perbangingan number
  num angka1 = 38;
  num angka2 = 83;

  print("Angka1 == Angka2 ${angka1 == angka2}");
  print("Angka1 != Angka2 ${angka1 != angka2}");
  print("Angka1 < Angka2 ${angka1 < angka2}");
  print("Angka1 > Angka2 ${angka1 > angka2}");
  print("Angka1 <= Angka2 ${angka1 <= angka2}");
  print("Angka1 >= Angka2 ${angka1 >= angka2}");
}
