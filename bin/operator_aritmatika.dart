void main(List<String> args) {
  var angka1 = 48;
  var angka2 = 4;

  // penjumlahan
  var value1 = angka1 + angka2;
  var value2 = angka1 - angka2;
  var value3 = angka1 * angka2;
  var value4 = angka1 / angka2;
  var value5 = angka1 ~/ angka2;
  var value56 = angka1 % angka2;

  print("Penjumlahan $angka1 + $angka2 = $value1");
  // pengurangan
  print("Pengurangan $angka1 - $angka2 = $value2");
  // perkalian
  print("Perkalian $angka1 * $angka2 = $value3");
  // pembagian hasil double
  print("Pembagian hasil double $angka1 / $angka2 = $value4");
  // pembagian hasil int
  print("Pembagian hasil int $angka1 ~/ $angka2 = $value5");
  // sisa bagi
  print("Sisa bagi $angka1 % $angka2 = $value56");
}
