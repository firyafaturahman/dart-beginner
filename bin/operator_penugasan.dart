void main(List<String> args) {
  num angka = 100;

  // operator aritmatika
  angka += 10;
  print(angka);
  angka -= 10;
  print(angka);
  angka *= 10;
  print(angka);
  angka /= 10;
  print(angka);
  angka ~/= 10;
  print(angka);
  angka %= 10;
  print(angka);

  num angka2 = 34;
  // increment
  print('increment');
  print(angka2++);
  print(++angka2);

  // decrement
  print('decrement');
  print(angka2--);
  print(--angka2);
}
